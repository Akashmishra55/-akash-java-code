public class NestedIf {
	public static void main(String args[]) {
		//declare 2 variables and store some values in it
		int num1 = 50;
		int num2 = 100;
		if( num1 == 50 )
		{
		if( num2 == 100 )
		{
		System.out.print("Number 1 is :"+ num1 +" and Number 2 is :"+ num2);
		} 
		} 
		else
		{
		System.out.print("Number 2 is not 100");
		}
	}
}//end of code

